package com.bean;
/*
 * 书类型：
 * 书类名
 * 书类编号
 */
public class Book_type {
	private String book_type_name;
	private String book_type_no;
	
	
	public Book_type() {

	}


	public String getBook_type_name() {
		return book_type_name;
	}


	public void setBook_type_name(String book_type_name) {
		this.book_type_name = book_type_name;
	}


	public String getBook_type_no() {
		return book_type_no;
	}


	public void setBook_type_no(String book_type_no) {
		this.book_type_no = book_type_no;
	}
	
}
